public class one {


    public static void main(String[] args) {
        int key =10;
        System.out.println(hash(key));
    }

    static final int hash(Object key) {
        int h;
        return (key == null) ? 0 : (h = key.hashCode()) ^ (h >>> 16);
    }



}
